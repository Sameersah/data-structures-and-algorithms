    public String getArrCity() {
        return arrCity;
    }

    public void setArrCity(String arrCity) {
        this.arrCity = arrCity;
    }

    public String getArrCtry() {
        return arrCtry;
    }

    public void setArrCtry(String arrCtry) {
        this.arrCtry = arrCtry;
    }

    public String getDepTim() {
        return depTim;
    }

    public void setDepTim(String depTim) {
        this.depTim = depTim;
    }

    public String getArrtTim() {
        return arrtTim;
    }

    public void setArrtTim(String arrtTim) {
        this.arrtTim = arrtTim;
    }
