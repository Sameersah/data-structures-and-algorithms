package com.sameer.algorithms.Node;

public class HuffmanBinaryTreeNode {

	HuffmanNode huffmanNode;
	HuffmanBinaryTreeNode left, right;
	public HuffmanNode getHuffmanNode() {
		return huffmanNode;
	}
	public void setHuffmanNode(HuffmanNode huffmanNode) {
		this.huffmanNode = huffmanNode;
	}
	public HuffmanBinaryTreeNode getLeft() {
		return left;
	}
	public void setLeft(HuffmanBinaryTreeNode left) {
		this.left = left;
	}
	public HuffmanBinaryTreeNode getRight() {
		return right;
	}
	public void setRight(HuffmanBinaryTreeNode right) {
		this.right = right;
	}
	
	
}
